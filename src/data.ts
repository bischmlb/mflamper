export type Lamp = typeof LAMPS[number]
export type LampColors = Lamp['colors']


export const getSortedVariants = (variant: Variant) => LAMPS.filter((lamp) => variant === lamp.variant)
export const getAssortmentId = (color: TwSprayColor) => Object.values(TwSprayColor).indexOf(color) + 1
export const getAssortmentIdsFromColors = (colors: LampColors) => {
    const lamp = LAMPS.find((lamp) => lamp.colors.every((color, id) => color === colors[id]))
    return lamp?.colors.map((color) => getAssortmentId(color))
}